    private void joinFamilySubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_joinFamilySubmitButtonActionPerformed
        
        
        try {
            // TODO add your handling code here:
            if (joinFamilyIDTextfield.getText().length() <= 0 || Integer.parseInt(joinFamilyIDTextfield.getText()) < 0){
                JOptionPane.showMessageDialog(this, "Invalid Input Try Again"); 
            } else { 
            
                joinFamID = joinFamilyIDTextfield.getText();
                fm.joinFamily(Integer.parseInt(joinFamID), null);
                if (fm.getFamilyIsExisting()){
                   new NewJFrame(fm).setVisible(true);
                   this.dispose();
                   this.setVisible(false);
                } 
                else
                   JOptionPane.showMessageDialog(this, "Family Doesn't Exist. Try Again.");

            }
  
          
        } catch (SQLException ex) {
            Logger.getLogger(OpenJframe.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NumberFormatException exe){ 
            JOptionPane.showMessageDialog(this, "Invalid Input Try Again");
        }
        
       
        
    }//GEN-LAST:event_joinFamilySubmitButtonActionPerformed

