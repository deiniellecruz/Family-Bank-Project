    private void signInSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signInSubmitButtonActionPerformed
        try {
            // TODO add your handling code here:
            if (signInUsernameTextfield.getText().length() <= 0 || signInPasswordTextfield.getText().length() <= 0){ 
                
                JOptionPane.showMessageDialog(this, "Invalid Input Try Again"); 
                
            } else { 
            
                signInUN = signInUsernameTextfield.getText();
                signInPass = signInPasswordTextfield.getText();

                fm.login(signInUN, signInPass);
                if (fm.getMemberIsExisting()){
                    new NewJFrame(fm).setVisible(true);
                    this.setVisible(false); 
                    this.dispose(); 
                }

                else
                    JOptionPane.showMessageDialog(this, "Invalid Username or Password");

            
            
            }   
           
        } catch (SQLException ex) {
            Logger.getLogger(OpenJframe.class.getName()).log(Level.SEVERE, null, ex);
        }
 
        
    }//GEN-LAST:event_signInSubmitButtonActionPerformed
  
    private void signUpSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signUpSubmitButtonActionPerformed
        // TODO add your handling code here:
        
        if (nameTextField.getText().length() <= 0 || signUpUsernameTextField.getText().length() <= 0 || signUpPasswordTextfield.getText().length() <= 0){
            
            JOptionPane.showMessageDialog(this, "Invalid Input Try Again");
            
        } else{ 
            
            signName = nameTextField.getText();
            signUpUN = signUpUsernameTextField.getText();
            signUpPass = signUpPasswordTextfield.getText();
            fm = new FamilyMember(signName, signUpUN, signUpPass);
        
            if (fm.getMember_alreadyExisting())
                JOptionPane.showMessageDialog(this, "Username is already taken");
            else 
                jTabbedPane.setSelectedIndex(3);
            
            
        }
        
        
        
      
    }//GEN-LAST:event_signUpSubmitButtonActionPerformed

